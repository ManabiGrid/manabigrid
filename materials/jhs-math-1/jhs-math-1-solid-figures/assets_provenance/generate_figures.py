# L06 図1: 立方体の見取図で「等しいのに等しく見えない」箇所の指摘図
# 本文根拠: lesson_06.md 主概念1（奥行きの面がつぶれる・長さと角は読めない）
# 注: 「実際は90°」は本文が図に載せると指定した注記（答え漏れではない）
# ===========================================================================
def fig_L06_1():
    # --- パラメータ ---
    a = 1.6            # 立方体の1辺（相対値）
    s = 74             # 描画スケール
    verts = box_verts(a, a, a)

    ck = Checker()
    E, F, G = verts["E"], verts["F"], verts["G"]
    ck.ok("正面の辺EFと奥行きの辺FGは実際は同じ長さ（立方体）",
          abs(v3len(v3sub(F, E)) - v3len(v3sub(G, F))) < EPS, f"1辺={a}")
    o = (108, 230)
    Epx, Fpx, Gpx = ob(o, s, E), ob(o, s, F), ob(o, s, G)
    len_front = math.hypot(Fpx[0] - Epx[0], Fpx[1] - Epx[1])
    len_depth = math.hypot(Gpx[0] - Fpx[0], Gpx[1] - Fpx[1])
    ck.ok("見取図上では長さが違って写る（奥行きは約半分）",
          abs(len_depth / len_front - 0.5) < 1e-6,
          f"正面{len_front:.0f}px/奥行き{len_depth:.0f}px")
    ang3d = math.degrees(math.acos(
        v3dot(v3sub(E, F), v3sub(G, F)) / (v3len(v3sub(E, F)) * v3len(v3sub(G, F)))))
    ve = (Epx[0] - Fpx[0], Epx[1] - Fpx[1])
    vg = (Gpx[0] - Fpx[0], Gpx[1] - Fpx[1])
    angpx = math.degrees(math.acos(
        (ve[0] * vg[0] + ve[1] * vg[1]) / (math.hypot(*ve) * math.hypot(*vg))))
    ck.ok("∠EFGは実際は90°なのに、見取図上では90°に見えない",
          abs(ang3d - 90) < EPS and abs(angpx - 135) < 1e-6,
          f"実際{ang3d:.0f}°/図上{angpx:.0f}°")

    cv = Canvas(520, 290)
    draw_box(cv, o, s, verts, labels=False)
    pticks(cv, *Epx, *Fpx, n=1)
    pticks(cv, *Fpx, *Gpx, n=1)
    pangle_arc(cv, Fpx[0], Fpx[1], 45, 180, r=15)
    cv.text_px(316, 232, "印の2辺は、", size=FS_SM)
    cv.text_px(316, 246, "実際はどちらも同じ長さ", size=FS_SM)
    arrow_px(cv, 312, 234, (Fpx[0] + Gpx[0]) / 2 + 10, (Fpx[1] + Gpx[1]) / 2 + 6, w=1.1)
    cv.text_px(316, 126, "この角は、実際は90°", size=FS_SM)
    arrow_px(cv, 314, 132, Fpx[0] + 18, Fpx[1] - 14, w=1.1)
    cv.text_px(240, 278, "見取図では、長さと角の実際の大きさは読み取れない",
               size=FS_CAP, anchor="middle")

    return dict(file="L06_fig1_oblique_limits.svg", canvas=cv, lesson="L06",
                title="立方体の見取図で「等しいのに等しく見えない」箇所の指摘図",
                intent="見取図の限界の明示。実際は等しい2辺（正面と奥行き）にティック、直角に見えない角に弧と注記",
                params=f"立方体1辺{a}（相対値）・斜投影で奥行きは1/2に写る（図上135°/実際90°）",
                checks=ck.items, forbid=["投影図", "展開図"])


