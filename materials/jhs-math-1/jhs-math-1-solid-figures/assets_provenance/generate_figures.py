# L10 図1: 円柱にぴったり入った球（半径r・高さ2rの寸法つき）
# 本文根拠: lesson_10.md 準備運動（底面の半径r・高さ2r・すき間なし）
# ===========================================================================
def fig_L10_1():
    # --- パラメータ ---
    r = 74             # 球＝円柱の底面半径(px)

    ck = Checker()
    ck.ok("円柱の底面の半径＝球の半径r（横にすき間なし）", True, f"r={r}px")
    ck.ok("円柱の高さ＝2r（上下にすき間なし・球の直径）",
          abs(2 * r - 2 * r) < EPS, f"2r={2 * r}px")

    cv = Canvas(430, 330)
    cx, ybase = 180, 268
    h = 2 * r
    cyl_px(cv, cx, ybase, r, h)
    # 内接する球（中心は円柱の中心・半径r）
    cy = ybase - r
    pcircle(cv, cx, cy, r, w=2.0)
    ell_half(cv, cx, cy, r, r * 0.30, "lower", w=AUX_W)
    ell_half(cv, cx, cy, r, r * 0.30, "upper", w=AUX_W, dash=DASH)
    # 寸法: 底面の半径r・高さ2r
    pline(cv, cx, ybase, cx + r, ybase, w=0.9)
    pdot(cv, cx, ybase, r=2.2)
    cv.text_px(cx + r / 2, ybase + 18, "r", size=FS, anchor="middle")
    pdim(cv, cx + r + 26, ybase, cx + r + 26, ybase - h, "2r", off=13)
    cv.text_px(215, 316, "横にも上下にもすき間なし", size=FS_CAP, anchor="middle")

    return dict(file="L10_fig1_sphere_in_cylinder.svg", canvas=cv, lesson="L10",
                title="円柱にぴったり入った球（半径r・高さ2r）",
                intent="比較の土台の視覚化。半径rの球が底面の半径r・高さ2rの円柱に内接（数値は載せない・文字rのみ）",
                params=f"r={r}px・高さ=2r={2 * r}pxを厳密一致（内接）",
                checks=ck.items, forbid=["cm", "π", "2/3"])


