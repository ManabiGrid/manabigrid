# L08 図2: 円錐の展開図（弧＝底面の円周・中心角120°）（例題4: r=3・母線9）
# 本文根拠: lesson_08.md 例題4（6π/18π=1/3 → 中心角120°。側面積27π等は書かない）
# ===========================================================================
def fig_L08_2():
    # --- パラメータ（lesson_08.md 例題4 と厳密一致） ---
    r_cm, l_cm = 3, 9            # 底面の半径・母線
    sp = 12.0                    # 1cmあたりpx
    center_ang = 360 * r_cm / l_cm   # 側面おうぎ形の中心角

    ck = Checker()
    ck.ok("中心角＝360°×(底面の半径/母線)＝120°",
          abs(center_ang - 120) < EPS, f"360×{r_cm}/{l_cm}={center_ang:.0f}")
    arc_len = 2 * math.pi * l_cm * center_ang / 360
    circ = 2 * math.pi * r_cm
    ck.ok("側面おうぎ形の弧の長さ＝底面の円周（どちらも6π）",
          abs(arc_len - circ) < EPS, f"={arc_len / math.pi:.0f}π")
    ck.ok("弧は半径9の円全体の円周18πの1/3", abs(arc_len * 3 - 2 * math.pi * l_cm) < EPS)

    cv = Canvas(560, 360)
    # 左: 見取図
    Rpx, rpx = l_cm * sp, r_cm * sp
    cone_px(cv, 92, 200, rpx + 10, 108)
    cv.text_px(66, 138, "母線9", size=FS_SM, anchor="end")
    pline(cv, 92, 200, 92 + rpx + 10, 200, w=0.9)
    cv.text_px(92 + rpx + 20, 198, "半径3", size=FS_SM)
    cv.text_px(92, 240, "円錐", size=FS_CAP, anchor="middle")
    # 右: 展開図（中心角120°のおうぎ形＋半径3の円）
    Cx, Cy = 360, 62
    a1, a2 = 270 - center_ang / 2, 270 + center_ang / 2   # 下向きに開く
    p1 = (Cx + Rpx * math.cos(math.radians(a1)), Cy - Rpx * math.sin(math.radians(a1)))
    p2 = (Cx + Rpx * math.cos(math.radians(a2)), Cy - Rpx * math.sin(math.radians(a2)))
    pline(cv, Cx, Cy, *p1, w=MAIN_W)
    pline(cv, Cx, Cy, *p2, w=MAIN_W)
    arc_open(cv, Cx, Cy, Rpx, a1, a2, w=BOLD_W)           # 弧を太線で強調
    pangle_arc(cv, Cx, Cy, a1, a2, r=16)
    cv.text_px(Cx, Cy + 34, "中心角120°", size=FS_SM, anchor="middle")
    cv.text_px((Cx + p2[0]) / 2 + 14, (Cy + p2[1]) / 2, "9", size=FS_SM)
    # 底面の円（弧のすぐ下・円周を太線で強調）
    bc = (Cx, Cy + Rpx + rpx + 18)
    pcircle(cv, *bc, rpx, w=BOLD_W)
    pline(cv, bc[0], bc[1], bc[0] + rpx, bc[1], w=0.9)
    cv.text_px(bc[0] + rpx / 2, bc[1] - 6, "3", size=FS_SM, anchor="middle")
    cv.text_px(bc[0], bc[1] + rpx + 22, "太線どうしは長さが等しい（どちらも6π）",
               size=FS_CAP, anchor="middle", weight="bold")

    return dict(file="L08_fig2_cone_net_120.svg", canvas=cv, lesson="L08",
                title="円錐の展開図（弧＝底面の円周・中心角120°）（例題4）",
                intent="「逆向きに考える」型の視覚化。半径9のおうぎ形の弧と半径3の底面円の円周を太線で対応づける（面積の答えは載せない）",
                params="例題4: 底面の半径3cm・母線9cm・中心角=360×3/9=120°を厳密反映・1cm=12px",
                checks=ck.items, forbid=["36", "27", "1/3"])


