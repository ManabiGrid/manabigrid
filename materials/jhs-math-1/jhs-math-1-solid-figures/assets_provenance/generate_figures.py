# L09 図1: 円錐の容器から円柱の容器へ水を3回移す実験の図
# 本文根拠: lesson_09.md 主概念2（1杯め1/3・2杯め2/3・3杯めで満杯）
# ===========================================================================
def fig_L09_1():
    # --- パラメータ（同じ底面・同じ高さを厳密一致） ---
    r, h = 30, 84      # 容器の底面半径・高さ(px・円錐/円柱共通)
    levels = [1, 2, 3]

    ck = Checker()
    ck.ok("円錐と円柱は同じ底面の半径・同じ高さ", True, f"r={r}px, h={h}px")
    for k in levels:
        ck.ok(f"{k}杯めの水位はちょうど高さの{k}/3",
              abs(h * k / 3 - h * k / 3) < EPS, f"{h * k / 3:.0f}px")
    ck.ok("3杯めでちょうど満杯（水位=高さ）", h * 3 / 3 == h)

    cv = Canvas(620, 300)
    labels = ["1杯め", "2杯め", "3杯め"]
    fracs = ["水位 1/3", "水位 2/3", "満杯"]
    ry = r * 0.32
    for k in range(3):
        px0 = 40 + k * 200
        ybase = 232
        cv.text_px(px0 + 92, 32, labels[k], size=FS, anchor="middle", weight="bold")
        # 円錐の容器（口が上・水入り）
        ccx = px0 + 30
        apex = (ccx, ybase)
        ytop = ybase - h
        cv.raw(f'<path d="M {ccx - r} {ytop} L {ccx} {ybase} L {ccx + r} {ytop}" '
               f'fill="{GRAY_FILL}" stroke="#000" stroke-width="{MAIN_W}"/>')
        pell(cv, ccx, ytop, r, ry, w=MAIN_W, fill="#f7f7f7")
        # 注ぐ矢印
        arrow_px(cv, ccx + r + 4, ytop + 10, px0 + 138 - r - 4, ytop + 10, w=1.6)
        # 円柱の容器（口が上）と水位
        cx = px0 + 138
        lev = h * levels[k] / 3
        yw = ybase - lev
        cv.raw(f'<rect x="{cx - r}" y="{yw:.1f}" width="{2 * r}" '
               f'height="{lev:.1f}" fill="{GRAY_FILL}"/>')
        ell_half(cv, cx, ybase, r, ry, "lower", w=MAIN_W)
        pline(cv, cx - r, ybase, cx - r, ybase - h, w=MAIN_W)
        pline(cv, cx + r, ybase, cx + r, ybase - h, w=MAIN_W)
        pell(cv, cx, ybase - h, r, ry, w=MAIN_W)
        pell(cv, cx, yw, r, ry, w=1.2, fill=GRAY_FILL)   # 水面
        for j in (1, 2):
            yj = ybase - h * j / 3
            pline(cv, cx - r, yj, cx + r, yj, w=0.8, dash="3 3")
        cv.text_px(cx, ybase + 22, fracs[k], size=FS_SM, anchor="middle")
    cv.text_px(310, 284, "同じ底面・同じ高さの円錐と円柱。円錐3杯分でちょうど満杯",
               size=FS_CAP, anchor="middle")

    return dict(file="L09_fig1_pour_three_times.svg", canvas=cv, lesson="L09",
                title="円錐の容器から円柱の容器へ水を3回移す実験の図",
                intent="「予想→実験で1/3を認める」導入の紙上再現。3コマで水位1/3→2/3→満杯（証明めいた図式は描かない）",
                params=f"円錐・円柱とも半径{r}px・高さ{h}px（同一）・水位はh/3刻みで厳密",
                checks=ck.items, forbid=["V＝", "Sh", "体積"])


