# 本文根拠: lesson_08.md 例題2（16π×2枚・8π×5=40π。合計72πは書かない）
# ===========================================================================
def fig_L08_1():
    # --- パラメータ（lesson_08.md 例題2 と厳密一致） ---
    r_cm, h_cm = 4, 5
    sp = 9.0                     # 1cmあたりpx
    circ = 2 * r_cm              # 円周の係数（8π の 8）
    base_area = r_cm ** 2        # 底面積の係数（16π の 16）
    side_area = circ * h_cm      # 側面積の係数（40π の 40）

    ck = Checker()
    ck.ok("円周＝2π×4＝8π（係数を検算）", circ == 8)
    ck.ok("底面積＝π×4²＝16π（係数を検算）", base_area == 16)
    ck.ok("側面積＝8π×5＝40π（係数を検算）", side_area == 40)
    ck.ok("合計72πは図に書かない（本文で計算させる）", True)

    rect_w = 2 * math.pi * r_cm * sp
    rect_h = h_cm * sp
    rpx = r_cm * sp
    ck.ok("長方形の横は2πr（px上も厳密一致）",
          abs(rect_w - 2 * math.pi * rpx) < EPS, f"{rect_w:.1f}px")

    cv = Canvas(620, 330)
    # 左: 見取図
    cyl_px(cv, 82, 190, rpx, rect_h)
    cv.text_px(82, 220, "円柱", size=FS_CAP, anchor="middle")
    # 右: 展開図
    nx = 380
    ny = 140
    x0 = nx - rect_w / 2
    ctop = (nx, ny - rpx - 12)
    pcircle(cv, *ctop, rpx, w=MAIN_W)
    pline(cv, ctop[0], ctop[1], ctop[0] + rpx, ctop[1], w=0.9)
    cv.text_px(ctop[0] + rpx / 2, ctop[1] - 6, "半径4", size=FS_SM, anchor="middle")
    cv.raw(f'<rect x="{x0:.1f}" y="{ny:.1f}" width="{rect_w:.1f}" height="{rect_h:.1f}" '
           f'fill="none" stroke="#000" stroke-width="{MAIN_W}"/>')
    cbot = (nx, ny + rect_h + rpx + 32)
    pcircle(cv, *cbot, rpx, w=MAIN_W)
    cv.text_px(nx + rect_w / 2 + 10, ny + rect_h / 2 + 4, "縦5", size=FS_SM)
    cv.text_px(nx, ny + rect_h + 16, "横＝円周8π", size=FS_SM, anchor="middle")
    # 部品ごとの面積の吹き出し
    cv.text_px(ctop[0] + rpx + 12, ctop[1] + 4, "底面積16π（が2枚）", size=FS_SM)
    cv.text_px(x0 - 10, ny + rect_h / 2 + 4, "側面積8π×5＝40π", size=FS_SM, anchor="end")
    cv.text_px(310, 316, "展開図をかく → 部品ごとに面積 → 合計する（単位はcm）",
               size=FS_CAP, anchor="middle")

    return dict(file="L08_fig1_cylinder_net_dims.svg", canvas=cv, lesson="L08",
                title="円柱の展開図と各部品の寸法書きこみ（例題2）",
                intent="表面積の型（展開→部品→合計）の視覚化。半径4・縦5・横=円周8πの寸法と部品の面積（合計72πは載せない）",
                params="例題2: 底面の半径4cm・高さ5cm・1cm=9px・長方形の横=2πrを厳密反映",
                checks=ck.items, forbid=["72", "94"])


