def pline(cv, x1, y1, x2, y2, w=MAIN_W, dash=None, color="#000"):
    d = f' stroke-dasharray="{dash}"' if dash else ""
    cv.raw(f'<line x1="{x1:.1f}" y1="{y1:.1f}" x2="{x2:.1f}" y2="{y2:.1f}" '
           f'stroke="{color}" stroke-width="{w}"{d}/>')


def ppoly(cv, pts, fill="none", w=MAIN_W, color="#000", dash=None, opacity=None):
    s = " ".join(f"{x:.1f},{y:.1f}" for x, y in pts)
    d = f' stroke-dasharray="{dash}"' if dash else ""
    op = f' fill-opacity="{opacity}"' if opacity is not None else ""
    cv.raw(f'<polygon points="{s}" fill="{fill}"{op} stroke="{color}" '
           f'stroke-width="{w}" stroke-linejoin="round"{d}/>')


def pdot(cv, x, y, r=DOT_R, color="#000"):
    cv.raw(f'<circle cx="{x:.1f}" cy="{y:.1f}" r="{r}" fill="{color}"/>')


def pcircle(cv, cx, cy, r, w=MAIN_W, fill="none", color="#000", dash=None):
    d = f' stroke-dasharray="{dash}"' if dash else ""
    cv.raw(f'<circle cx="{cx:.1f}" cy="{cy:.1f}" r="{r:.1f}" fill="{fill}" '
           f'stroke="{color}" stroke-width="{w}"{d}/>')


def pell(cv, cx, cy, rx, ry, w=MAIN_W, fill="none", color="#000", dash=None):
    d = f' stroke-dasharray="{dash}"' if dash else ""
    cv.raw(f'<ellipse cx="{cx:.1f}" cy="{cy:.1f}" rx="{rx:.1f}" ry="{ry:.1f}" '
           f'fill="{fill}" stroke="{color}" stroke-width="{w}"{d}/>')


def ell_half(cv, cx, cy, rx, ry, half="lower", w=MAIN_W, color="#000", dash=None):
    """楕円の下半分（half=lower・見取図の手前弧）/上半分（upper・奥弧）"""
    sweep = 0 if half == "lower" else 1
    d = f' stroke-dasharray="{dash}"' if dash else ""
    cv.raw(f'<path d="M {cx - rx:.1f} {cy:.1f} A {rx:.1f} {ry:.1f} 0 0 {sweep} '
           f'{cx + rx:.1f} {cy:.1f}" fill="none" stroke="{color}" stroke-width="{w}"{d}/>')


def sector(cv, cx, cy, r, a1, a2, fill=GRAY_FILL, w=MAIN_W, color="#000"):
    """中心角つき扇形（角度は数学式・度・a1<a2・反時計回り）"""
    p1 = (cx + r * math.cos(math.radians(a1)), cy - r * math.sin(math.radians(a1)))
    p2 = (cx + r * math.cos(math.radians(a2)), cy - r * math.sin(math.radians(a2)))
    laf = 1 if (a2 - a1) > 180 else 0
    cv.raw(f'<path d="M {cx:.1f} {cy:.1f} L {p1[0]:.1f} {p1[1]:.1f} '
           f'A {r:.1f} {r:.1f} 0 {laf} 0 {p2[0]:.1f} {p2[1]:.1f} Z" '
           f'fill="{fill}" stroke="{color}" stroke-width="{w}" stroke-linejoin="round"/>')


def arc_open(cv, cx, cy, r, a1, a2, w=MAIN_W, color="#000"):
    """弧のみ（角度は数学式・度・a1<a2・反時計回り）"""
    p1 = (cx + r * math.cos(math.radians(a1)), cy - r * math.sin(math.radians(a1)))
    p2 = (cx + r * math.cos(math.radians(a2)), cy - r * math.sin(math.radians(a2)))
    laf = 1 if (a2 - a1) > 180 else 0
    cv.raw(f'<path d="M {p1[0]:.1f} {p1[1]:.1f} A {r:.1f} {r:.1f} 0 {laf} 0 '
           f'{p2[0]:.1f} {p2[1]:.1f}" fill="none" stroke="{color}" stroke-width="{w}"/>')


def pticks(cv, x1, y1, x2, y2, n=1, half=4.5, gap=3.5):
    """対応する辺のティック（px直指定）"""
    mx, my = (x1 + x2) / 2, (y1 + y2) / 2
    dx, dy = x2 - x1, y2 - y1
    L = math.hypot(dx, dy)
    ux, uy = dx / L, dy / L
    nx, ny = -uy, ux
    for i in range(n):
        off = (i - (n - 1) / 2) * gap
        cx, cy = mx + ux * off, my + uy * off
        pline(cv, cx - nx * half, cy - ny * half, cx + nx * half, cy + ny * half, w=1.4)


def prightangle(cv, vx, vy, u, w_, size=8.0):
    """頂点(vx,vy)の直角マーク。u・w_はpx方向ベクトル"""
    lu, lw = math.hypot(*u), math.hypot(*w_)
    ux, uy = u[0] / lu * size, u[1] / lu * size
    wx, wy = w_[0] / lw * size, w_[1] / lw * size
    cv.raw(f'<polyline points="{vx + ux:.1f},{vy + uy:.1f} '
           f'{vx + ux + wx:.1f},{vy + uy + wy:.1f} {vx + wx:.1f},{vy + wy:.1f}" '
           f'fill="none" stroke="#000" stroke-width="1.2"/>')


def pangle_arc(cv, vx, vy, a1, a2, r=14.0, n=1, gap=3.5, w=1.2):
    """頂点の角の弧（角度は数学式・度・a1<a2）をn重に"""
    for k in range(n):
        rr = r + k * gap
        pts = []
        for i in range(21):
            t = math.radians(a1 + (a2 - a1) * i / 20)
            pts.append(f"{vx + rr * math.cos(t):.1f},{vy - rr * math.sin(t):.1f}")
        cv.raw(f'<polyline points="{" ".join(pts)}" fill="none" '
               f'stroke="#000" stroke-width="{w}"/>')


def pdim(cv, x1, y1, x2, y2, label, off=14.0, tick=4.0, size=FS_CAP):
    """寸法線: 細線+両端ティック+法線側ラベル（px直指定）"""
    pline(cv, x1, y1, x2, y2, w=0.9)
    dx, dy = x2 - x1, y2 - y1
    L = math.hypot(dx, dy)
    nx, ny = -dy / L, dx / L
    for (x, y) in ((x1, y1), (x2, y2)):
        pline(cv, x - nx * tick, y - ny * tick, x + nx * tick, y + ny * tick, w=0.9)
    mx, my = (x1 + x2) / 2, (y1 + y2) / 2
    cv.text_px(mx + nx * off, my + ny * off + size * 0.35, label,
               size=size, anchor="middle")


def arrow_px(cv, x1, y1, x2, y2, w=1.4, head=7.0):
    """矢印（線+先端の三角形・px直指定）"""
    ang = math.atan2(y2 - y1, x2 - x1)
    bx, by = x2 - head * math.cos(ang), y2 - head * math.sin(ang)
    pline(cv, x1, y1, bx, by, w=w)
    nx, ny = -math.sin(ang), math.cos(ang)
    cv.raw(f'<polygon points="{x2:.1f},{y2:.1f} '
           f'{bx + nx * head * 0.45:.1f},{by + ny * head * 0.45:.1f} '
           f'{bx - nx * head * 0.45:.1f},{by - ny * head * 0.45:.1f}" fill="#000"/>')


# ---- 斜投影（キャビネット図: 奥行きを1/2・45°右上へ） ---------------------
OBK = 0.5 / math.sqrt(2)   # 奥行き1単位あたりのx/y成分（合成で1/2）


def ob(o, s, p3):
    """3D点(x,y,z)（y上・z奥）→px。o=(ox,oy)は3D原点のpx位置"""
    return (o[0] + s * (p3[0] + OBK * p3[2]), o[1] - s * (p3[1] + OBK * p3[2]))


def v3sub(a, b):
    return (a[0] - b[0], a[1] - b[1], a[2] - b[2])


def v3cross(a, b):
    return (a[1] * b[2] - a[2] * b[1], a[2] * b[0] - a[0] * b[2],
            a[0] * b[1] - a[1] * b[0])


def v3dot(a, b):
    return a[0] * b[0] + a[1] * b[1] + a[2] * b[2]


def v3len(a):
    return math.sqrt(v3dot(a, a))


def cyl_px(cv, cx, ybase, r, h, ry=None, w=MAIN_W, color="#000",
           base_fill=None, top_fill=None):
    """立てた円柱の見取図（隠れ弧は破線）"""
    ry = ry if ry is not None else r * 0.32
    yt = ybase - h
    if base_fill:
        pell(cv, cx, ybase, r, ry, w=0.0001, fill=base_fill, color="none")
    if top_fill:
        pell(cv, cx, yt, r, ry, w=0.0001, fill=top_fill, color="none")
    pline(cv, cx - r, yt, cx - r, ybase, w=w, color=color)
    pline(cv, cx + r, yt, cx + r, ybase, w=w, color=color)
    pell(cv, cx, yt, r, ry, w=w, color=color)
    ell_half(cv, cx, ybase, r, ry, "lower", w=w, color=color)
    ell_half(cv, cx, ybase, r, ry, "upper", w=AUX_W, color=color, dash=DASH)
    return dict(r=r, h=h, ry=ry, cx=cx, ybase=ybase)


def cone_px(cv, cx, ybase, r, h, ry=None, w=MAIN_W, color="#000", base_fill=None):
    """円錐の見取図（底面の奥弧は破線）"""
    ry = ry if ry is not None else r * 0.32
    yt = ybase - h
    if base_fill:
        pell(cv, cx, ybase, r, ry, w=0.0001, fill=base_fill, color="none")
    pline(cv, cx, yt, cx - r, ybase, w=w, color=color)
    pline(cv, cx, yt, cx + r, ybase, w=w, color=color)
    ell_half(cv, cx, ybase, r, ry, "lower", w=w, color=color)
    ell_half(cv, cx, ybase, r, ry, "upper", w=AUX_W, color=color, dash=DASH)
    return dict(r=r, h=h, apex=(cx, yt))


def sphere_px(cv, cx, cy, r, w=MAIN_W, color="#000", fill="none"):
    """球の見取図（輪郭円＋赤道の楕円: 手前実線・奥破線）"""
    pcircle(cv, cx, cy, r, w=w, color=color, fill=fill)
    ell_half(cv, cx, cy, r, r * 0.32, "lower", w=AUX_W, color=color)
    ell_half(cv, cx, cy, r, r * 0.32, "upper", w=AUX_W, color=color, dash=DASH)


def _hidden_bottom_vertex(base_xz):
    """見取図で隠れる底面頂点（最も奥・左寄り）のindex"""
    return max(range(len(base_xz)), key=lambda i: base_xz[i][1] * 2 - base_xz[i][0])


def prism_px(cv, o, s, base_xz, h, w=MAIN_W, base_fill=None):
    """角柱の見取図（底面base_xz=(x,z)列・高さh・隠れ線は破線）"""
    n = len(base_xz)
    bot = [(x, 0.0, z) for x, z in base_xz]
    top = [(x, h, z) for x, z in base_xz]
    hid = _hidden_bottom_vertex(base_xz)
    if base_fill:
        ppoly(cv, [ob(o, s, p) for p in bot], fill=base_fill, w=0.0001, color="none")
    for i in range(n):
        j = (i + 1) % n
        dash = DASH if (i == hid or j == hid) else None
        a, b = ob(o, s, bot[i]), ob(o, s, bot[j])
        pline(cv, *a, *b, w=AUX_W if dash else w, dash=dash)
        a, b = ob(o, s, top[i]), ob(o, s, top[j])
        pline(cv, *a, *b, w=w)
    for i in range(n):
        dash = DASH if i == hid else None
        a, b = ob(o, s, bot[i]), ob(o, s, top[i])
        pline(cv, *a, *b, w=AUX_W if dash else w, dash=dash)
    return bot, top


def pyramid_px(cv, o, s, base_xz, h, w=MAIN_W, base_fill=None):
    """角錐の見取図（頂点は底面重心の真上・隠れ線は破線）"""
    n = len(base_xz)
    bot = [(x, 0.0, z) for x, z in base_xz]
    gx = sum(x for x, z in base_xz) / n
    gz = sum(z for x, z in base_xz) / n
    apex = (gx, h, gz)
    hid = _hidden_bottom_vertex(base_xz)
    if base_fill:
        ppoly(cv, [ob(o, s, p) for p in bot], fill=base_fill, w=0.0001, color="none")
    for i in range(n):
        j = (i + 1) % n
        dash = DASH if (i == hid or j == hid) else None
        a, b = ob(o, s, bot[i]), ob(o, s, bot[j])
        pline(cv, *a, *b, w=AUX_W if dash else w, dash=dash)
    for i in range(n):
        dash = DASH if i == hid else None
        a, b = ob(o, s, bot[i]), ob(o, s, apex)
        pline(cv, *a, *b, w=AUX_W if dash else w, dash=dash)
    return bot, apex


def plane_patch(cv, o, s, x0, x1, z0, z1, y=0.0, fill=GRAY_FILL, w=MAIN_W,
                opacity=None, color="#000"):
    """水平面（y=一定）の平行四辺形パッチを斜投影で描く"""
    pts3 = [(x0, y, z0), (x1, y, z0), (x1, y, z1), (x0, y, z1)]
    ppoly(cv, [ob(o, s, p) for p in pts3], fill=fill, w=w,
          opacity=opacity, color=color)
    return pts3


# ---- 直方体ABCD-EFGH（L03共通の舞台: 本文と頂点配置を厳密一致） ------------
def box_verts(w, h, d):
    """上面ABCD・下面EFGH・AはEの真上（lesson_03本文と一致）"""
    return dict(A=(0, h, 0), B=(w, h, 0), C=(w, h, d), D=(0, h, d),
                E=(0, 0, 0), F=(w, 0, 0), G=(w, 0, d), H=(0, 0, d))


BOX_EDGES = ["AB", "BC", "CD", "DA", "EF", "FG", "GH", "HE",
             "AE", "BF", "CG", "DH"]
BOX_HIDDEN = {"EH", "GH", "DH"}   # 隠れ線（正規化キー: ソート済み2文字）


def ekey(e):
    return "".join(sorted(e))


def classify_edges(verts, ref="AB"):
    """基準辺に対する 交わる/平行/ねじれ の3分類（3Dベクトルで判定）"""
    ra, rb = verts[ref[0]], verts[ref[1]]
    rd = v3sub(rb, ra)
    out = {"交わる": [], "平行": [], "ねじれ": []}
    for e in BOX_EDGES:
        if ekey(e) == ekey(ref):
            continue
        if set(e) & set(ref):
            out["交わる"].append(e)
            continue
        a, b = verts[e[0]], verts[e[1]]
        d = v3sub(b, a)
        if v3len(v3cross(rd, d)) < EPS:
            out["平行"].append(e)
        else:
            # ねじれ＝平行でなく交わらない（4点が同一平面上にないことも検算）
            assert abs(v3dot(v3cross(rd, d), v3sub(a, ra))) > EPS, f"{e}が同一平面上"
            out["ねじれ"].append(e)
    return out


def draw_box(cv, o, s, verts, style=None, labels=True, label_dist=13):
    """直方体を描く。style: ekey→(w,color) 上書き。隠れ線は破線"""
    cpx = [0.0, 0.0]
    for v in verts.values():
        p = ob(o, s, v)
        cpx[0] += p[0] / 8
        cpx[1] += p[1] / 8
    drawn = 0
    for e in BOX_EDGES:
        a, b = ob(o, s, verts[e[0]]), ob(o, s, verts[e[1]])
        wdt, col = (style or {}).get(ekey(e), (MAIN_W, "#000"))
        dash = DASH if ekey(e) in BOX_HIDDEN else None
        pline(cv, *a, *b, w=(wdt if not dash else max(wdt * 0.8, AUX_W)),
              dash=dash, color=col)
        drawn += 1
    if labels:
        for nm, v in verts.items():
            x, y = ob(o, s, v)
            dx, dy = x - cpx[0], y - cpx[1]
            L = math.hypot(dx, dy) or 1.0
            cv.text_px(x + dx / L * label_dist, y + dy / L * label_dist + FS * 0.35,
                       nm, size=FS, anchor="middle", weight="bold")
    return drawn


