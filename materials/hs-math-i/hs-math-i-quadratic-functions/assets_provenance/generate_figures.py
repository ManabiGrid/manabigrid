# 図3: L02 §1 上下に動かす——y=x²＋q
# 図の採用根拠: 「どのxでもちょうど2大きい→全体が+2ずれる」は表と4文の文章のみ。
# 平行移動の前後比較の第1図（縦方向）。
# ===========================================================================
def fig_L02_1():
    # --- パラメータ（lesson_02.md §1 と一致させる） ---
    f1 = lambda x: x * x
    f2 = lambda x: x * x + 2
    table_x = [-2, -1, 0, 1, 2]
    arrows_at = [-1.5, 0, 1.5]            # ＋2のずれを見せる縦矢印の位置

    ck = Checker()
    on_curve(ck, "y=x²", f1, [(x, x * x) for x in table_x])
    on_curve(ck, "y=x²+2", f2, [(x, x * x + 2) for x in table_x])
    ck.ok("どのxでも差がちょうど2（本文の表 4,1,0,1,4 → 6,3,2,3,6 と一致）",
          all(f2(x) - f1(x) == 2 for x in table_x) and
          [f2(x) for x in table_x] == [6, 3, 2, 3, 6])
    ck.ok("移動後の頂点(0,2)・軸は直線x=0のまま", f2(0) == 2 and f2(1) == f2(-1))

    cv = plane((-2.9, 2.9), (-0.8, 7.2), 34.0, 26.0, ml=38, mr=76, mt=16, mb=26)
    draw_axes(cv, xnums=(-2, -1, 1, 2), ynums=(2, 4, 6))
    for f in (f1, f2):
        xa, xb = vspan(1, 0, 7.2 - (0 if f is f1 else 2), -2.9, 2.9)
        cv.polyline(func_pts(f, xa, xb), w=MAIN_W)
    for t in arrows_at:
        (x0, y0), (x1, y1) = cv.P((t, f1(t))), cv.P((t, f2(t)))
        arrow_px(cv, x0, y0, x1, y1 + 5, w=1.3)
    tx, ty = cv.P((1.5, (f1(1.5) + f2(1.5)) / 2))
    cv.text_px(tx + 10, ty + 4, "＋2", size=12, anchor="start", weight="bold")
    cv.dot((0, 0), r=3.0)
    cv.dot((0, 2), r=3.0)
    lx, ly = cv.P((math.sqrt(7.2), 6.9))
