# 図2: L01 §5 aの符号と開き方
# 図の採用根拠: 符号（凸の向き）と絶対値（開き方）の2性質が文章+箇条書きのみで、
# 3本の曲線の比較は文字では目が滑る。x軸対称の実例(1,2)-(1,−2)も本文どおり示す。
