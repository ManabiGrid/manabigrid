def fig_L12_1():
    # --- パラメータ（lesson_12.md 例題1 と一致させる） ---
    dom = (0, 10)                          # 定義域 0<x<10（両端含まない）
    sol = (3, 7)                           # 不等式の解 3≦x≦7（両端含む）

    ck = Checker()
    ck.ok("(x−3)(x−7)≦0の解は3≦x≦7（端点で=0・間で負・外で正）",
          (3 - 3) * (3 - 7) == 0 and (5 - 3) * (5 - 7) < 0 and (2 - 3) * (2 - 7) > 0)
    ck.ok("解3≦x≦7は定義域0<x<10にすべて収まる（重ねても削れない）",
          dom[0] < sol[0] and sol[1] < dom[1])
    ck.ok("検算: x=3で面積3×7=21≧21・x=5で25≧21・x=2で16<21",
          3 * 7 == 21 and 5 * 5 == 25 >= 21 and 2 * 8 == 16 < 21)

    W, H = 620, 216
    cv = Canvas(W, H, sx=1.0)
