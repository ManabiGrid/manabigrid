    arrow_px(cv, ox + 40, oy + 20, ox + 5, oy + 4, w=1.1, head=5.5)
    lx, ly = cv.P((2.6, 8.2))
    cv.text_px(lx + 8, ly, "y＝x²", size=FS, anchor="start")

