def fig_L01_2():
    # --- パラメータ（lesson_01.md §5 と一致させる） ---
    curves = [(2.0, "y＝2x²"), (1.0, "y＝x²"), (-2.0, "y＝−2x²")]
    pair = ((1, 2), (1, -2))              # 本文: x=1でy=2とy=−2（符号が逆）

    ck = Checker()
    for a, name in curves:
        on_curve(ck, name, lambda x, a=a: a * x * x,
                 [(x, a * x * x) for x in (-2, -1, 0, 1, 2)])
    ck.ok("x=1でy=2x²は2・y=−2x²は−2（すべてのxでyの符号が逆＝x軸対称）",
          2 * 1 == 2 and -2 * 1 == -2 and all(2 * x * x == -(-2 * x * x) for x in range(-3, 4)))
    ck.ok("|a|が大きいほど開き方が狭い: 同じy=2に達するxは y=2x²で±1・y=x²で±√2",
          math.isclose(math.sqrt(2 / 2.0), 1.0) and math.sqrt(2 / 1.0) > 1.0)

    cv = plane((-3.2, 3.2), (-8.6, 8.6), 30.0, 13.0, ml=40, mr=66, mt=16, mb=26)
    draw_axes(cv, xnums=(-2, -1, 1, 2), ynums=(-8, -4, 2, 4, 8))
    for a, name in curves:
        xa, xb = vspan(a, 0, 8.6, -3.2, 3.2)
        cv.polyline(func_pts(lambda x, a=a: a * x * x, xa, xb), w=MAIN_W)
    # x軸対称の実例（破線で結ぶ）
    cv.line(pair[0], pair[1], w=AUX_W, dash=DASH)
    cv.dot(pair[0], r=3.0)
    cv.dot(pair[1], r=3.0)
    # 式ラベル（y軸ラベルと重ならない左腕側に置く）
    lx, ly = cv.P((-math.sqrt(8.6 / 2), 8.2))
    cv.text_px(lx - 6, ly, "y＝2x²", size=FS, anchor="end")
    lx, ly = cv.P((math.sqrt(8.6), 8.2))
    cv.text_px(lx + 6, ly, "y＝x²", size=FS, anchor="start")
    lx, ly = cv.P((math.sqrt(8.6 / 2), -8.2))
    cv.text_px(lx + 6, ly + 8, "y＝−2x²", size=FS, anchor="start")

    return {"file": "L01_fig2_sign_and_width.svg", "lesson": "L01", "canvas": cv,
            "title": "aの符号と開き方——y=2x²・y=x²・y=−2x²",
            "intent": "a>0は下に凸・a<0は上に凸、|a|が大きいほど狭い——3本の比較とx軸対称の実例(1,2)(1,−2)を1枚で見せる",
            "src": "lesson_01.md §5（箇条書きと開き方の文の直後）",
            "params": "a=2,1,−2／破線(1,2)-(1,−2)／式ラベル3つ",
            "checks": ck.items, "check_tokens": [],
            "anchor_file": "lesson_01.md",
            "anchor": "また、aの絶対値が大きいほどグラフの開き方は狭くなる。y=2x² は y=x² より細い放物線である。"}


